import 'package:flutter/material.dart';
import 'coin_data.dart';

class PriceScreen extends StatefulWidget {
  @override
  _PriceScreenState createState() => _PriceScreenState();
}

class _PriceScreenState extends State<PriceScreen> {
  String currency = currenciesList[0];
  Map rates = {'BTC':0, 'ETH':0, 'LTC':0};

  List<DropdownMenuItem<String>> buildListOfCurrencies() {
    List<DropdownMenuItem<String>> currencies = [];
    for (String c in currenciesList) {
      currencies.add(DropdownMenuItem(child: Text(c), value: c,));
    }
    return currencies;
  }

  Future<void> getExchangeRates() async {
    rates = await CoinData.getRates(currency);
  }

  @override
  void initState() {
    super.initState();
    getExchangeRates();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('ðŸ¤‘ Coin Ticker'),
      ),
      body: Column(
